import java.util.Scanner;

public class questao_01 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        int n;

        System.out.println("Digite um valor para N: ");
        n = sc.nextInt();


        for (int i = 1; i <= n; i++) {

            System.out.print(i + " ");

        }
sc.close();

}
}
